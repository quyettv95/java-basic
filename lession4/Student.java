package lession4;

public class Student {
    String code;
    String fullname;
    String address;
    String gender;
    float score;

    Student()
    {

    }

    Student(String code, String fullname, String address, String gender, float score)
    {
        this.code = code;
        this.fullname = fullname;
        this.address = address;
        this.gender = gender;
        this.score = score;
    }

}
